import math

def calculate_factorial(n):
    """
    Вычисляет факториал числа n с использованием библиотеки math.
    """
    return math.factorial(n)

def main():
    try:
        # Запрашиваем ввод у пользователя
        user_input = input("Введите положительное целое число: ")
        
        # Преобразуем ввод в целое число
        number = int(user_input)
        
        # Проверяем, что число положительное
        if number < 0:
            print("Ошибка: Введено отрицательное число. Пожалуйста, введите положительное целое число.")
        else:
            # Вычисляем факториал
            result = calculate_factorial(number)
            
            # Выводим результат
            print(f"Факториал числа {number} равен: {result}")
    
    except ValueError:
        # Обработка ошибки, если введены нечисловые данные
        print("Ошибка: Введены нечисловые данные. Пожалуйста, введите положительное целое число.")

if __name__ == "__main__":
    main()