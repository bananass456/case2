import math

def compute_factorial():
    """
    Функция для вычисления факториала с обработкой ошибок и повторным запросом ввода.
    """
    while True:
        try:
            # Запрашиваем ввод у пользователя
            num = input("Введите положительное целое число: ")
            
            # Проверка, является ли введенная строка числом
            num = int(num)
            
            # Проверка на отрицательные числа
            if num < 0:
                print("Ошибка: введено отрицательное число. Введите положительное число.")
            else:
                # Вычисление факториала с помощью библиотеки math
                result = math.factorial(num)
                print(f"Факториал числа {num} равен {result}")
                break  # Выход из цикла при успешном вводе
        except ValueError:
            # Обработка ошибки, если введено не число
            print("Ошибка: введены некорректные данные. Пожалуйста, введите целое число.")

# Вызов функции compute_factorial
compute_factorial()
